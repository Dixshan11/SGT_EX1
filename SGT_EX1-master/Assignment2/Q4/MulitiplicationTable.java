

public class MulitiplicationTable {
    public static void main(String[] args) {
        System.out.println("using for loop");
        for(int i=1;i<=10;i++){
            System.out.println(i+" * "+i+" = "+i*i);
        }
        System.out.println(" ");
        System.out.println("using while loop");
        int i = 1;
        while (i <=10) {
            System.out.println(i+" * "+i+" = "+i*i);
            i++;
        }
    }


}
